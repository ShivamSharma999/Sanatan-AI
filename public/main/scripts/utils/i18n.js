let currentLocale = defaultLocale;
let currentTranslations = locales[defaultLocale];
function formatTemplate(text, vars) {
    if (typeof text !== 'string' || !vars)
        return text;
    return Object.keys(vars).reduce((result, key) => {
        return result.replace(new RegExp(`{${key}}`, 'g'), vars[key]);
    }, text);
}
export function getLocale() {
    return currentLocale;
}
export function t(key, vars) {
    const value = currentTranslations[key] ?? locales[fallbackLocale][key] ?? key;
    if (Array.isArray(value))
        return value;
    return formatTemplate(value, vars);
}
export function translatePage() {
    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.dataset.i18n;
        const text = t(key);
        if (text === undefined)
            return;
        if (element.dataset.i18nHtml !== undefined) {
            element.innerHTML = text;
        }
        else if (element.dataset.i18nPlaceholder !== undefined) {
            element.placeholder = text;
        }
        else if (element.dataset.i18nValue !== undefined) {
            element.value = text;
        }
        else if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.value = text;
        }
        else if (element.tagName === 'OPTION') {
            element.textContent = text;
        }
        else {
            element.textContent = text;
        }
    });
    document.querySelectorAll('[data-i18n-label]').forEach((element) => {
        const text = t(element.dataset.i18nLabel);
        element.dataset.label = text;
    });
    document.querySelectorAll('[data-i18n-aria]').forEach((element) => {
        element.setAttribute('aria-label', t(element.dataset.i18nAria));
    });
    document.querySelectorAll('[data-i18n-title]').forEach((element) => {
        element.title = t(element.dataset.i18nTitle);
    });
}
export function setLocale(locale) {
    if (!locales[locale])
        locale = defaultLocale;
    currentLocale = locale;
    currentTranslations = locales[locale];
    localStorage.setItem('sanatan_locale', currentLocale);
    document.documentElement.lang = currentLocale;
    translatePage();
    return currentLocale;
}
export function initI18n() {
    const savedLocale = localStorage.getItem('sanatan_locale');
    const browserLocale = navigator.language?.split('-')[0];
    const locale = savedLocale || (locales[browserLocale] ? browserLocale : defaultLocale);
    setLocale(locale);
}
initI18n();
